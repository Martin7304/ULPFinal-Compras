    private void botonNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonNuevoActionPerformed
        // NUEVO PRODUCTO
        AccesoProducto ad = new AccesoProducto();
        Producto p = new Producto();
        p.setNombre(jtextNombre.getText());
        p.setDescripcion(jtextDescripcion.getText());
        //PRECIO ACTUAL CAMBIAR A DOUBLE 
        p.setPrecioActual(Integer.parseInt(jtextPrecio.getText()));
        p.setStock(Integer.parseInt(jtextStock.getText()));
        p.setEstado(true);
        ad.guardarProducto(p);
        
    }//GEN-LAST:event_botonNuevoActionPerformed

    private void botonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonModificarActionPerformed
        // MODIFICAR PRODUCTO
        AccesoProducto ad = new AccesoProducto();
        Producto p = new Producto();
        p.setNombre(jtextNombre.getText());
        p.setDescripcion(jtextDescripcion.getText());
        //PRECIO ACTUAL CAMBIAR A DOUBLE 
        p.setPrecioActual(Integer.parseInt(jtextPrecio.getText()));
        p.setStock(Integer.parseInt(jtextStock.getText()));
        
        if (jrbEstado.isSelected()) {
            p.setEstado(true);
        } else {
            p.setEstado(false);
        }
        
        ad.modificarProduto(p);
    }//GEN-LAST:event_botonModificarActionPerformed


